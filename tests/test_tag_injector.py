def _make_minimal_mp3() -> bytes:
    """
    Create a minimal MP3 with an empty ID3 header using mutagen.
    Falls back to None if mutagen is not installed.
    """
    try:
        from mutagen.id3 import ID3  # type: ignore[import]
    except ImportError:
        return b""

    buf = io.BytesIO()
    id3 = ID3()
    id3.save(buf, v2_version=3)
    # Append a minimal valid MPEG sync frame so mutagen recognises it as MP3
    buf.write(b"\xff\xfb\x90\x00" + b"\x00" * 413)  # 417-byte MPEG1 frame
    buf.seek(0)
    return buf.read()


class TestTagInjectorMp3:
    def test_inject_adds_txxx_frames(self) -> None:
        mp3 = _make_minimal_mp3()
        if not mp3:
            pytest.skip("mutagen not installed")

        result = _make_result(grade="A")
        tagged = TagInjector().inject(mp3, result)

        from mutagen.id3 import ID3  # type: ignore[import]
        id3 = ID3(io.BytesIO(tagged))
        txxx_descs = [frame.desc for frame in id3.getall("TXXX")]
        assert "SYNC_SAFE_VERDICT" in txxx_descs
        assert "SYNC_SAFE_VERSION" in txxx_descs

    def test_inject_adds_comm_frame(self) -> None:
        mp3 = _make_minimal_mp3()
        if not mp3:
            pytest.skip("mutagen not installed")

        tagged = TagInjector().inject(mp3, _make_result())
        from mutagen.id3 import ID3  # type: ignore[import]
        id3    = ID3(io.BytesIO(tagged))
        comm   = id3.getall("COMM")
        assert any(c.desc == "Sync-Safe Report" for c in comm)

    def test_inject_result_is_larger_than_original(self) -> None:
        mp3 = _make_minimal_mp3()
        if not mp3:
            pytest.skip("mutagen not installed")

        tagged = TagInjector().inject(mp3, _make_result())
        assert len(tagged) >= len(mp3)
