class TestDetectFormat:
    def test_id3_header(self) -> None:
        assert _detect_format(b"ID3" + b"\x00" * 100) == "mp3"

    def test_mp3_sync_frame(self) -> None:
        # 0xFF 0xFB = MPEG1 Layer3 128kbps stereo sync word
        assert _detect_format(b"\xff\xfb" + b"\x00" * 100) == "mp3"

    def test_flac_magic(self) -> None:
        assert _detect_format(b"fLaC" + b"\x00" * 100) == "flac"

    def test_ogg_magic(self) -> None:
        assert _detect_format(b"OggS" + b"\x00" * 100) == "ogg"

    def test_unknown_returns_none(self) -> None:
        assert _detect_format(b"\x00" * 100) is None

    def test_empty_returns_none(self) -> None:
        assert _detect_format(b"") is None


